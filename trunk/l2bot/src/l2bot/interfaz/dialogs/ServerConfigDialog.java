    public void servers(){
        
        parsear();
        
        //System.out.print(((Element)f.item(0)).getAttribute("host"));
        list1.removeAll();
        for(int i=0;i<f.getLength();i++){
            Element h = (Element)f.item(i);
            //System.out.print(h.getAttribute("id"));
            list1.add(h.getAttribute("name"));
            list1.select(i);
        }
        
    }
    
    public void parsear(){
        DOMParser parser = new DOMParser();
        try {
            parser.parse("xml/servers.xml");
        } catch (SAXException ex) {
            Logger.getLogger(ServerConfigDialog.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ServerConfigDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        Document doc = parser.getDocument();
        f = doc.getElementsByTagName("server");
    }

