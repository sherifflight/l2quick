private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    if(list1.getSelectedIndex() < 0){
    JOptionPane.showMessageDialog( this,"Tiene que seleccionar un servidor");
    return;
    }
    if(JOptionPane.showConfirmDialog( this,"Â¿Estas seguro?","confirmacion",JOptionPane.YES_NO_OPTION ) == JOptionPane.NO_OPTION){
        return;
    }
    
    try{
        FileWriter w=new FileWriter("xml/servers.xml");
        w.write("<servers>\n");
        for(int i=0;i<f.getLength();i++){
            Element h = (Element) f.item(i);
             if(!list1.getSelectedItem().equals(h.getAttribute("name"))){
                w.write("     <server name='"+h.getAttribute("name")+"' host='"+h.getAttribute("host")+"' port='"+h.getAttribute("port")+"' gameservers='"+h.getAttribute("gameservers")+"'/>\n");
             }
        }
        w.write("</servers>\n");
        w.flush();
        w.close();
        list1.remove(list1.getSelectedIndex());
    }catch(IOException e)
    {}
}//GEN-LAST:event_jButton4ActionPerformed

